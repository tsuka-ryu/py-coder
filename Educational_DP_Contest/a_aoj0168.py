while True:
    n = int(input())
    if n == 0:
        break

    # dp[i] = i段目に到達する方法の総数
    dp = [0] * (n + 1)
    dp[0] = 1  # 0段目(地面)にいる方法は1通り

    for i in range(1, n + 1):
        # i段目には以下の3通りの方法で到達できる
        if i >= 1:
            dp[i] += dp[i - 1]  # 1段登る
        if i >= 2:
            dp[i] += dp[i - 2]  # 2段登る
        if i >= 3:
            dp[i] += dp[i - 3]  # 3段登る

    # 全ての登り方を試すのに必要な日数
    total_ways = dp[n]
    # print(*dp)
    days_needed = (total_ways + 9) // 10  # 1日10種類、切り上げ

    # 年数に変換(1日でも必要なら1年)
    years_needed = (days_needed + 364) // 365

    print(years_needed)

